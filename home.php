        <h2>Introduction</h2>

        <img class="right" src="images/language_buttons.jpg" width="250" alt="Colourful flag buttons"/>

        <p>
            Languages around the world often have words that cannot be directly translated into English using just one word. These are known as untranslatable words. They usually describe feelings, experiences, or cultural ideas that do not have an exact match in other languages.
        </p>

        <p>
            This website explores untranslatable words from many different languages. For each word, you’ll find its meaning explained in clear English, along with an example of how it might be used. By learning these words, we can better understand the ways people from different cultures think, feel, and communicate.
        </p>

        <p>
            Untranslatable words remind us that language is more than just vocabulary — it reflects how we see the world. Exploring them can help us appreciate the richness and variety of human expression.
        </p>